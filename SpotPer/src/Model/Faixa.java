package Model;

/**
 *
 * @author Tibet Teixeira
 */
public class Faixa {

    int idAlbum;
    int numFaixa;
    int idComposicao;
    float tempoDuracao;
    String descricao;
    String tipoGravacao;
    String descricaoComposicao;

    public String getDescricaoComposicao() {
        return descricaoComposicao;
    }

    public void setDescricaoComposicao(String descricaoComposicao) {
        this.descricaoComposicao = descricaoComposicao;
    }

    public int getIdAlbum() {
        return idAlbum;
    }

    public void setIdAlbum(int idAlbum) {
        this.idAlbum = idAlbum;
    }

    public int getNumFaixa() {
        return numFaixa;
    }

    public void setNumFaixa(int numFaixa) {
        this.numFaixa = numFaixa;
    }

    public int getIdComposicao() {
        return idComposicao;
    }

    public void setIdComposicao(int idComposicao) {
        this.idComposicao = idComposicao;
    }

    public float getTempoDuracao() {
        return tempoDuracao;
    }

    public void setTempoDuracao(float tempoDuracao) {
        this.tempoDuracao = tempoDuracao;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getTipoGravacao() {
        return tipoGravacao;
    }

    public void setTipoGravacao(String tipoGravacao) {
        this.tipoGravacao = tipoGravacao;
    }

}
