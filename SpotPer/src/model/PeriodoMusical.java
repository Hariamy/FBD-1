package Model;

/**
 *
 * @author Tibet Teixeira
 */
public class PeriodoMusical {
    int idPeriodo;
    String inicioPeriodo;
    String fimPeriodo;
    String descricao;

    public int getIdPeriodo() {
        return idPeriodo;
    }

    public void setIdPeriodo(int idPeriodo) {
        this.idPeriodo = idPeriodo;
    }

    public String getInicioPeriodo() {
        return inicioPeriodo;
    }

    public void setInicioPeriodo(String inicioPeriodo) {
        this.inicioPeriodo = inicioPeriodo;
    }

    public String getFimPeriodo() {
        return fimPeriodo;
    }

    public void setFimPeriodo(String fimPeriodo) {
        this.fimPeriodo = fimPeriodo;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
    
    
}
