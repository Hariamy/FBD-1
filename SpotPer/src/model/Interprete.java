public class Interprete {
    int idInterprete;
    int idTipoInterprete;
    String nome;

    public int getIdInterprete() {
        return idInterprete;
    }

    public void setIdInterprete(int idInterprete) {
        this.idInterprete = idInterprete;
    }

    public int getIdTipoInterprete() {
        return idTipoInterprete;
    }

    public void setIdTipoInterprete(int idTipoInterprete) {
        this.idTipoInterprete = idTipoInterprete;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    
}
